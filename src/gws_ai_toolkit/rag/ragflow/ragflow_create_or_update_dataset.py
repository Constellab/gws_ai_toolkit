from gws_ai_toolkit.rag.ragflow.ragflow_class import (
    RagFlowCreateDatasetRequest,
    RagFlowUpdateDatasetRequest,
)
from gws_ai_toolkit.rag.ragflow.ragflow_service import RagFlowService
from gws_core import (
    ConfigParams,
    ConfigSpecs,
    CredentialsDataOther,
    CredentialsParam,
    CredentialsType,
    IntParam,
    OutputSpec,
    OutputSpecs,
    StrParam,
    Task,
    TaskInputs,
    TaskOutputs,
    Text,
    TypingStyle,
    task_decorator,
)


@task_decorator(
    "RagFlowCreateOrUpdateDataset",
    human_name="Create or Update RagFlow Dataset",
    short_description="Create a new or update an existing dataset (knowledgebase) in RagFlow",
    style=TypingStyle.community_image("ragflow", "#4A90E2"),
)
class RagFlowCreateOrUpdateDataset(Task):
    """
    [Generated by Task Expert Agent]

    Create a new dataset (knowledgebase) or update an existing one in RagFlow.

    If a **Dataset ID** is provided, the existing dataset is updated with the given parameters.
    Otherwise, a new dataset is created.

    ## Usage
    - **Output**: A Text resource containing the dataset ID

    ## Configuration
    - `api_key`: RagFlow credentials (route + api_key)
    - `name`: Name of the dataset
    - `parse_method`: Chunking method (default: "naive")
    - `chunk_token_num`: Recommended chunk size in tokens (default: 1024)
    - `delimiter`: Delimiter used to split text into chunks (default: "##")
    - `dataset_id`: (Optional) Existing dataset ID to update instead of creating a new one
    """

    output_specs = OutputSpecs(
        {
            "dataset_id": OutputSpec(
                Text,
                human_name="Dataset ID",
                short_description="The ID of the created or updated RagFlow dataset",
            ),
        }
    )

    config_specs = ConfigSpecs(
        {
            "api_key": CredentialsParam(
                credentials_type=CredentialsType.OTHER,
                human_name="RagFlow API Key",
                short_description="A credentials that contains 'route' and 'api_key'",
            ),
            "name": StrParam(
                human_name="Dataset name",
                short_description="Name of the dataset",
            ),
            "parse_method": StrParam(
                default_value="naive",
                allowed_values=[
                    "naive",
                    "manual",
                    "qa",
                    "table",
                    "paper",
                    "book",
                    "laws",
                    "resume",
                ],
                human_name="Parse method",
                short_description="Chunking method used to parse documents",
            ),
            "chunk_token_num": IntParam(
                default_value=1024,
                min_value=1,
                human_name="Chunk token size",
                short_description="Recommended chunk size in tokens",
            ),
            "delimiter": StrParam(
                default_value="##",
                human_name="Delimiter",
                short_description="Delimiter used to split text into chunks",
            ),
            "dataset_id": StrParam(
                default_value=None,
                optional=True,
                human_name="Dataset ID",
                short_description="Optional existing dataset ID — if provided, the dataset is updated instead of created",
            ),
        }
    )

    def run(self, params: ConfigParams, inputs: TaskInputs) -> TaskOutputs:
        credentials: CredentialsDataOther = params.get_value("api_key")
        ragflow_service = RagFlowService.from_credentials(credentials)

        name: str = params.get_value("name")
        parse_method: str = params.get_value("parse_method")
        chunk_token_num: int = params.get_value("chunk_token_num")
        delimiter: str = params.get_value("delimiter")
        dataset_id: str | None = params.get_value("dataset_id")

        if dataset_id:
            self.log_info_message(f"Updating existing dataset '{dataset_id}'...")

            update_request = RagFlowUpdateDatasetRequest(
                name=name,
                parse_method=parse_method,
                chunk_token_num=chunk_token_num,
                delimiter=delimiter,
            )
            dataset = ragflow_service.update_dataset(dataset_id, update_request)

            self.log_success_message(f"Dataset updated: {dataset.id}")
        else:
            self.log_info_message(f"Creating dataset '{name}'...")

            create_request = RagFlowCreateDatasetRequest(
                name=name,
                parse_method=parse_method,
                chunk_token_num=chunk_token_num,
                delimiter=delimiter,
            )
            dataset = ragflow_service.create_dataset(create_request)

            self.log_success_message(f"Dataset created with ID: {dataset.id}")

        return {"dataset_id": Text(dataset.id)}
